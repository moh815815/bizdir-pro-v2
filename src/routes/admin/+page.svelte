  taglineEn: "The Region's Premier Business Directory",
  description: 'منصة B2B متكاملة تجمع أفضل الشركات والمحلات التجارية',
  contact: {
    email: 'info@bizdir.pro',
    phone: '+966 50 000 0000',
    address: 'الرياض، المملكة العربية السعودية',
    social: {
      twitter: 'https://twitter.com/bizdirpro',
      linkedin: 'https://linkedin.com/company/bizdirpro',
      instagram: 'https://instagram.com/bizdirpro',
    }
  }
};

export const categories = ${JSON.stringify(categories, null, 2)};

export const listings = ${JSON.stringify(listings.map(l => ({
      ...l,
      tags: Array.isArray(l.tags) ? l.tags : l.tags.split(',').map(t => t.trim()).filter(Boolean),
      services: Array.isArray(l.services) ? l.services : l.services.split(',').map(s => s.trim()).filter(Boolean),
    })), null, 2)};

export function getListingBySlug(slug) {
  return listings.find(l => l.slug === slug) ?? null;
}
export function getListingsByCategory(categoryId) {
  return listings.filter(l => l.category === categoryId);
}
export function getFeaturedListings(limit = 6) {
  return listings.filter(l => l.featured).slice(0, limit);
}
export function searchListings({ query = '', category = '', city = '', priceRange = '' } = {}) {
  const q = query.toLowerCase();
  return listings.filter(l => {
    const matchQuery = !q || l.name.toLowerCase().includes(q) || l.nameEn.toLowerCase().includes(q) || l.description.toLowerCase().includes(q) || l.tags.some(t => t.toLowerCase().includes(q));
    const matchCat   = !category   || l.category   === category;
    const matchCity  = !city       || l.cityEn     === city || l.city === city;
    const matchPrice = !priceRange || l.priceRange  === priceRange;
    return matchQuery && matchCat && matchCity && matchPrice;
  });
}
export const cities = [...new Set(listings.map(l => l.cityEn))];
export const priceRanges = ['$', '$$', '$$$', '$$$$'];
`;
    const blob = new Blob([code], { type: 'text/javascript' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url; a.download = 'store.js'; a.click();
    URL.revokeObjectURL(url);
    showToast('تم تصدير store.js — انسخه إلى src/lib/data/ ✅');
  }

  // ── Stats ─────────────────────────────────────────────────────
  $: stats = {
    total: listings.length,
    featured: listings.filter(l => l.featured).length,
    verified: listings.filter(l => l.verified).length,
    cats: [...new Set(listings.map(l => l.category))].length,
  };

  $: filtered = listings.filter(l => {
    const q = searchQ.toLowerCase();
    const mq = !q || l.name.toLowerCase().includes(q) || (l.nameEn||'').toLowerCase().includes(q);
    const mc = !filterCat || l.category === filterCat;
    return mq && mc;
  });

  const priceRanges = ['$', '$$', '$$$', '$$$$'];
</script>

