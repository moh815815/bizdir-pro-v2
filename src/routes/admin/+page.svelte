<script>
  import { onMount } from 'svelte';
  import { categories } from '$lib/data/store.js';

  // ── Auth ──────────────────────────────────────────────────────
  const ADMIN_USER = 'admin';
  const ADMIN_PASS = 'bizdir2025';

  let authed = false;
  let loginUser = '';
  let loginPass = '';
  let loginErr  = '';

  function doLogin() {
    if (loginUser === ADMIN_USER && loginPass === ADMIN_PASS) {
      authed = true;
      loginErr = '';
      if (typeof localStorage !== 'undefined')
        localStorage.setItem('bizdir_admin', '1');
    } else {
      loginErr = 'اسم المستخدم أو كلمة المرور غير صحيحة';
    }
  }
  function doLogout() {
    authed = false;
    if (typeof localStorage !== 'undefined')
      localStorage.removeItem('bizdir_admin');
  }

  // ── State ─────────────────────────────────────────────────────
  let tab = 'dashboard'; // dashboard | listings | add | settings
  let listings = [];
  let toast = '';
  let toastType = 'success';

  function showToast(msg, type = 'success') {
    toast = msg; toastType = type;
    setTimeout(() => toast = '', 3200);
  }

  // ── Persist ───────────────────────────────────────────────────
  function save() {
    if (typeof localStorage !== 'undefined')
      localStorage.setItem('bizdir_listings', JSON.stringify(listings));
  }
  function load() {
    if (typeof localStorage === 'undefined') return;
    const s = localStorage.getItem('bizdir_listings');
    if (s) listings = JSON.parse(s);
    if (localStorage.getItem('bizdir_admin')) authed = true;
  }

  onMount(load);

  // ── Blank form ────────────────────────────────────────────────
  function blank() {
    return {
      id: 'lst-' + Date.now(),
      slug: '',
      name: '', nameEn: '',
      category: '',
      subcategory: '',
      description: '', descriptionEn: '',
      rating: 4.5, reviewCount: 0,
      priceRange: '$$',
      priceMin: 0, priceMax: 0,
      city: '', cityEn: '',
      address: '',
      phone: '', email: '', website: '',
      verified: true, featured: false,
      tags: '',
      services: '',
      openHours: '',
      image: '',
      established: new Date().getFullYear(),
    };
  }

  let form = blank();
  let editId = null;
  let deleteConfirm = null;
  let searchQ = '';
  let filterCat = '';

  // ── Slug auto-gen ─────────────────────────────────────────────
  function toSlug(s) {
    return s.toLowerCase().replace(/\s+/g, '-').replace(/[^\w-]/g, '').substring(0, 60);
  }
  $: if (form.nameEn && !editId) form.slug = toSlug(form.nameEn);

  // ── CRUD ──────────────────────────────────────────────────────
  function submitForm() {
    if (!form.name || !form.category || !form.slug) {
      showToast('يرجى ملء الحقول المطلوبة: الاسم، التصنيف، المعرّف', 'error'); return;
    }
    const entry = {
      ...form,
      tags: form.tags.split(',').map(t => t.trim()).filter(Boolean),
      services: form.services.split(',').map(s => s.trim()).filter(Boolean),
      rating: parseFloat(form.rating) || 4.5,
      reviewCount: parseInt(form.reviewCount) || 0,
      priceMin: parseInt(form.priceMin) || 0,
      priceMax: parseInt(form.priceMax) || 0,
      established: parseInt(form.established) || 2020,
    };
    if (editId) {
      listings = listings.map(l => l.id === editId ? entry : l);
      editId = null;
      showToast('تم تحديث السجل بنجاح ✅');
    } else {
      listings = [entry, ...listings];
      showToast('تمت إضافة السجل بنجاح ✅');
    }
    save();
    form = blank();
    tab = 'listings';
  }

  function startEdit(l) {
    editId = l.id;
    form = {
      ...l,
      tags: Array.isArray(l.tags) ? l.tags.join(', ') : l.tags,
      services: Array.isArray(l.services) ? l.services.join(', ') : l.services,
    };
    tab = 'add';
  }

  function confirmDelete(id) { deleteConfirm = id; }
  function doDelete() {
    listings = listings.filter(l => l.id !== deleteConfirm);
    deleteConfirm = null;
    save();
    showToast('تم حذف السجل', 'error');
  }

  function toggleFeatured(id) {
    listings = listings.map(l => l.id === id ? { ...l, featured: !l.featured } : l);
    save();
  }
  function toggleVerified(id) {
    listings = listings.map(l => l.id === id ? { ...l, verified: !l.verified } : l);
    save();
  }

  // ── Export store.js ───────────────────────────────────────────
  function exportStore() {
    const code = `// Auto-generated by BizDir Pro Admin — ${new Date().toLocaleString('ar')}
// Copy this file to: src/lib/data/store.js

export const siteConfig = {
