// Helper: get listing by slug
export function getListingBySlug(slug) {
  return listings.find(l => l.slug === slug) ?? null;
}

// Helper: get listings by category
export function getListingsByCategory(categoryId) {
  return listings.filter(l => l.category === categoryId);
}

// Helper: get featured listings
export function getFeaturedListings(limit = 6) {
  return listings.filter(l => l.featured).slice(0, limit);
}

// Helper: search listings
export function searchListings({ query = '', category = '', city = '', priceRange = '' } = {}) {
  const q = query.toLowerCase();
  return listings.filter(l => {
    const matchQuery =
      !q ||
      l.name.toLowerCase().includes(q) ||
      l.nameEn.toLowerCase().includes(q) ||
      l.description.toLowerCase().includes(q) ||
      l.tags.some(t => t.toLowerCase().includes(q));
    const matchCat   = !category   || l.category   === category;
    const matchCity  = !city       || l.cityEn     === city || l.city === city;
    const matchPrice = !priceRange || l.priceRange  === priceRange;
    return matchQuery && matchCat && matchCity && matchPrice;
  });
}

